  methods = map_new(String, MsgpackRpcRequestHandler)();

  msgpack_rpc_add_method_handler((String) {.data = "buffer_line_count", .size = sizeof("buffer_line_count") - 1}, (MsgpackRpcRequestHandler) {.fn = handle_buffer_line_count, .async = false});
